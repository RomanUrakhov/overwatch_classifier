def learning(a, b, normal_mode=True):
    for i in range(b.shape[0]):
        for j in range(a.shape[1]):
            if i == j:
                a[:, j] += b[i]
            else:
                a[:, j] -= b[i]
            if normal_mode:
                normalization(a)
    result = np.copy(a)
    return result


def normalization(array):
    while True:
        max_index = np.unravel_index(np.argmax(array, axis=None), array.shape)
        min_index = np.unravel_index(np.argmin(array, axis=None), array.shape)
        if abs(array[max_index]) > 1:
            array[max_index] /= array[max_index]
        elif abs(array[min_index]) > 1:
            array[min_index] /= array[min_index]
        else:
            return array


def get_demons_vector(concept, model):
    return np.dot(concept, model)


def get_prevailing_demons(demons_vector):
    return np.where(demons_vector == demons_vector.max())[0]


