def get_class(concept_list, model, chars_dict):
    model = np.array(model)
    concept_list = np.array(concept_list)
    demons = get_demons_vector(concept_list, model)
    max_demon_indices = get_prevailing_demons(demons)
    potential_characters = []
    for i, key in enumerate(chars_dict):
        if i in max_demon_indices:
            potential_characters.append(key)
    return potential_characters


def export_model(filename, model):
    with open(filename, mode='w', newline='') as out_file:
        writer = csv.writer(out_file)
        writer.writerows(model)


def import_model(filename):
    data = []
    try:
        with open(filename, newline='') as input_file:
            reader = csv.reader(input_file)
            for row in reader:
                data.append(row)
    except FileNotFoundError as e:
        print(e)
    return data


